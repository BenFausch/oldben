           <h2 style="text-align:left">About the Book</h2>
            <p>
           In <em>For the Love of Rivers</em>, Kurt Fausch draws readers into an international collaboration among stream ecologists to discover the strong hidden connections between rivers and their surrounding forests.
            </p>
            <br>
            <p>
But the tragic loss of one of his closest Japanese colleagues, and the decline of rivers and fishes 
that Fausch and his colleagues study, forced him to look beyond the water&rsquo;s surface and ask 
what is essential about rivers, and why humans would want to conserve them.
            </p>
   <br>
      <p>Fausch states, &ldquo;Like trees and music and good health, streams and rivers are a gift to us as humans&hellip; In the end, I believe we will need to understand how and why we love rivers, if we hope to conserve them.&rdquo; </p>
