<h1>About Ben</h1>
<p>Ben Fausch grew up in Fort Collins, Colorado, where he spent all of his time playing music and learning as many instruments as possible. He graduated from the University of Colorado at Boulder with a degree in Music Performance and Technology. He spent a decade working in the music industry as a touring performer, recording engineer, and event promoter.
</p>
<p>
While working in music PR, Ben discovered a passion for the technical side of the industry, publishing websites for clients, designing promotional materials, and executing social media campaigns.
This new direction brought him to New York City, where he earned a MA degree in Media Studies (with an emphasis in interactive design and web development) at The New School.
</p>
<p>
While in NYC, Ben worked for several world-renowned recording studios and independent record labels, but discovered an unexpected spark during an interactive design class.
</p>
<p>
    After that course, that spark grew into a full-burning addiction. He began living and breathing code, obsessively tackling new languages and skills. Ever since, Ben has been working as a front-end web developer and a freelance designer. He currently lives in Denver with his wife and two tubas. He still composes, records, and performs regularly. </p>

